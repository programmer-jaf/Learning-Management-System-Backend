import { Schema, model } from 'mongoose';

// --------------------------------------------------
// Custom-Modules
// --------------------------------------------------
import { ICart } from '@interfaces/cart.interfaces';

const cartSchema = new Schema<ICart>(
  {
    user: {
      type: Schema.Types.ObjectId,
      ref: 'User',
      required: true,
    },
    items: [
      {
        course: {
          type: Schema.Types.ObjectId,
          ref: 'Course',
          required: true,
        },
        price: {
          type: Number,
          required: true,
        },
      },
    ],
    totalPrice: {
      type: Number,
      default: 0,
    },
  },
  { timestamps: true }
);

// --------------------------------------------------
// Cart Model
// --------------------------------------------------
export const CartModel = model<ICart>('Cart', cartSchema);
